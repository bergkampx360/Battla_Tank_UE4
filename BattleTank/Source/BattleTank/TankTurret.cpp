#include "BattleTank.h"
#include "TankTurret.h"

void UTankTurret::Rotate(float TurnRate)
{
	TurnRate = FMath::Clamp<float>(TurnRate, -1, 1);
	auto RotationChange = TurnRate * MaxDegreesPerSecond * GetWorld()->DeltaTimeSeconds;
	auto Rotation = RelativeRotation.Yaw + RotationChange;
	SetRelativeRotation(FRotator(0, Rotation, 0));
}
